#include "libft.h"

int	ft_memcmp(const void	*s1, const void	*s2, size_t	n)
{
	size_t				i;
	unsigned char		*p2;
	unsigned char		*p1;

	p2 = (unsigned char *)s2;
	p1 = (unsigned char *)s1;
	i = 0;
	while (i < n)
	{
		if (p1[i] != p2[i])
		{
			return (p1[i] - p2[i]);
		}
		i++;
	}
	return (0);
}
